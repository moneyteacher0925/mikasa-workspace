"""셀러부트캠프 1~4주차 PPT 생성기 v2 - 확장판 (주차당 80~100슬라이드)"""

from pptx import Presentation
from pptx.util import Inches, Pt, Emu
from pptx.dml.color import RGBColor
from pptx.enum.text import PP_ALIGN

BLACK = RGBColor(0, 0, 0)
WHITE = RGBColor(0xFF, 0xFF, 0xFF)
RED = RGBColor(0xFF, 0, 0)
GRAY = RGBColor(0x66, 0x66, 0x66)

SLIDE_W = Emu(12192000)
SLIDE_H = Emu(6858000)
FONT = '맑은 고딕'

def wm(slide):
    t = slide.shapes.add_textbox(Emu(SLIDE_W - Inches(2.8)), Emu(SLIDE_H - Inches(0.5)), Inches(2.5), Inches(0.4))
    p = t.text_frame.paragraphs[0]
    p.text = "셀러 부트캠프"
    p.alignment = PP_ALIGN.RIGHT
    r = p.runs[0]; r.font.size = Pt(14); r.font.color.rgb = GRAY; r.font.name = FONT

def ns(prs):
    s = prs.slides.add_slide(prs.slide_layouts[6])
    s.background.fill.solid(); s.background.fill.fore_color.rgb = BLACK
    wm(s); return s

def cover(prs, week, title):
    s = ns(prs)
    for txt, y, sz, clr in [(f"WEEK {week}", 1.5, 60, RED), (title, 3.5, 40, WHITE), ("셀러 부트캠프", 5.2, 20, GRAY)]:
        t = s.shapes.add_textbox(Inches(0.5), Inches(y), Inches(11.5), Inches(1.5))
        p = t.text_frame.paragraphs[0]; p.text = txt; p.alignment = PP_ALIGN.CENTER
        r = p.runs[0]; r.font.size = Pt(sz); r.font.color.rgb = clr; r.font.name = FONT; r.font.bold = (clr != GRAY)

def section(prs, name, sub=""):
    s = ns(prs)
    t = s.shapes.add_textbox(Inches(0.8), Inches(2.0), Inches(11), Inches(1.5))
    p = t.text_frame.paragraphs[0]; p.text = name; p.alignment = PP_ALIGN.CENTER
    r = p.runs[0]; r.font.size = Pt(40); r.font.color.rgb = RED; r.font.name = FONT; r.font.bold = True
    if sub:
        t2 = s.shapes.add_textbox(Inches(1), Inches(3.8), Inches(10.5), Inches(1))
        p2 = t2.text_frame.paragraphs[0]; p2.text = sub; p2.alignment = PP_ALIGN.CENTER
        r2 = p2.runs[0]; r2.font.size = Pt(24); r2.font.color.rgb = WHITE; r2.font.name = FONT

def impact(prs, text, color=WHITE, notes=""):
    s = ns(prs)
    t = s.shapes.add_textbox(Inches(0.8), Inches(1.5), Inches(11), Inches(4))
    tf = t.text_frame; tf.word_wrap = True
    p = tf.paragraphs[0]; p.text = text; p.alignment = PP_ALIGN.CENTER
    r = p.runs[0]; r.font.size = Pt(44); r.font.color.rgb = color; r.font.name = FONT; r.font.bold = True
    if notes: s.notes_slide.notes_text_frame.text = notes

def bullets(prs, title, items, notes="", title_color=WHITE):
    s = ns(prs)
    t = s.shapes.add_textbox(Inches(0.8), Inches(0.5), Inches(11), Inches(1.2))
    tf = t.text_frame; tf.word_wrap = True
    p = tf.paragraphs[0]; p.text = title; p.alignment = PP_ALIGN.LEFT
    r = p.runs[0]; r.font.size = Pt(36); r.font.color.rgb = title_color; r.font.name = FONT; r.font.bold = True
    t2 = s.shapes.add_textbox(Inches(1.0), Inches(2.0), Inches(10.5), Inches(4.5))
    tf2 = t2.text_frame; tf2.word_wrap = True
    for i, b in enumerate(items):
        p2 = tf2.paragraphs[0] if i == 0 else tf2.add_paragraph()
        p2.text = f"• {b}" if not b.startswith(("☐","✅","→","  ")) else b
        p2.space_after = Pt(12)
        for rr in p2.runs: rr.font.size = Pt(24); rr.font.color.rgb = WHITE; rr.font.name = FONT
    if notes: s.notes_slide.notes_text_frame.text = notes

def mission(prs, title, items, notes=""):
    s = ns(prs)
    t = s.shapes.add_textbox(Inches(0.8), Inches(0.5), Inches(11), Inches(1.2))
    p = t.text_frame.paragraphs[0]; p.text = f"📋 {title}"; p.alignment = PP_ALIGN.LEFT
    r = p.runs[0]; r.font.size = Pt(36); r.font.color.rgb = RED; r.font.name = FONT; r.font.bold = True
    t2 = s.shapes.add_textbox(Inches(1.0), Inches(2.0), Inches(10.5), Inches(4.5))
    tf2 = t2.text_frame; tf2.word_wrap = True
    for i, item in enumerate(items):
        p2 = tf2.paragraphs[0] if i == 0 else tf2.add_paragraph()
        p2.text = f"☐ {item}"; p2.space_after = Pt(14)
        for rr in p2.runs: rr.font.size = Pt(26); rr.font.color.rgb = WHITE; rr.font.name = FONT
    if notes: s.notes_slide.notes_text_frame.text = notes

def cp(): p = Presentation(); p.slide_width = SLIDE_W; p.slide_height = SLIDE_H; return p

